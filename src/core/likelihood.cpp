// RAII timer for likelihood evaluation. Capture wall time on construction;
// stamp_into() writes it into the result's evaluation_time field. Used inline
// at dispatch sites instead of a wrapping callable — keeps the if-constexpr
// branches shallow and avoids return-type deduction issues across the variant.
struct evaluation_timer {
    std::chrono::steady_clock::time_point t0 = std::chrono::steady_clock::now();
    template <class StateLike>
    void stamp_into(StateLike& s) const {
        auto dt = std::chrono::duration<double>(std::chrono::steady_clock::now() - t0).count();
        get<evaluation_time>(s)() = dt;
    }
};

// Append a double, a Matrix<double>, or a SymPosDefMatrix<double> as raw
// little-endian doubles to an open binary stream. Helpers for the bootstrap
// samples binary writer below.
inline void write_doubles_binary(std::ofstream& f, double x) {
    f.write(reinterpret_cast<const char*>(&x), sizeof(double));
}
inline void write_doubles_binary(std::ofstream& f, const Matrix<double>& m) {
    for (std::size_t i = 0; i < m.nrows(); ++i)
        for (std::size_t j = 0; j < m.ncols(); ++j) {
            double x = m(i, j);
            f.write(reinterpret_cast<const char*>(&x), sizeof(double));
        }
}
inline void write_doubles_binary(std::ofstream& f, const SymPosDefMatrix<double>& m) {
    for (std::size_t i = 0; i < m.nrows(); ++i)
        for (std::size_t j = 0; j < m.ncols(); ++j) {
            double x = m(i, j);
            f.write(reinterpret_cast<const char*>(&x), sizeof(double));
        }
}

