// Multi-simulation loop. All simulations are at the same θ, so the FuncMap_St
// cache is shareable across them — the macro Qdt and the (now memoized) micro
// Qdt convolutions get computed once per (Agonist_step, fs[, Nchannels]) instead
// of once per simulation. For n_simulations = 4 this is a 4× reduction on the
// hot path. Numerical FIM is left untouched (different θ per call there).
//
// `memoize=false` swaps to the no-memoization function table, useful for
// correctness cross-checks. The two table makers return different types, so
// the dispatch is at construction; the visit helper is templated on FuncTable.
auto calculate_n_simulation_mdlikelihood_predictions_impl(
    const likelihood_algorithm_type& modelLikelihood_v, const var::Parameters_transformed& par,
    const Experiment& e, const std::vector<Simulated_Recording<var::please_include<>>>& simulations)
    -> Maybe_error<std::vector<dMacro_State_Ev_gradient_all>> {
    bool memoize = resolve_memoize_flag(true);

    auto run_loop = [&](auto& ftbl3) -> Maybe_error<std::vector<dMacro_State_Ev_gradient_all>> {
        const std::size_t n = simulations.size();
        // Per-thread function-table forks: each thread gets its own Qdt
        // memoization cache, so the per-simulation loop parallelizes without a
        // cache data race. (Same f.fork(omp_get_max_threads()) idiom as the
        // sampling code in qmodel.h.) This is the active parallel level only
        // when the combo loop is serial (MACRODR_AXIS_SERIAL=1); otherwise it
        // nests and runs serially.
        auto forks = ftbl3.fork(omp_get_max_threads());
        std::vector<std::optional<dMacro_State_Ev_gradient_all>> slots(n);
        std::vector<std::string> errs(n);
#pragma omp parallel for schedule(static)
        for (std::size_t i = 0; i < n; ++i) {
            auto& ftbl = forks[static_cast<std::size_t>(omp_get_thread_num())];
            auto res = calculate_mdlikelihood_predictions_visit(
                modelLikelihood_v, ftbl, par, e, get<Recording>(simulations[i]()));
            if (res)
                slots[i].emplace(std::move(res.value()));
            else
                errs[i] = res.error()();
        }
        std::vector<dMacro_State_Ev_gradient_all> results;
        results.reserve(n);
        for (std::size_t i = 0; i < n; ++i) {
            if (!errs[i].empty()) return error_message(errs[i]);
            results.push_back(std::move(*slots[i]));
        }
        return results;
    };

    if (memoize) {
        auto ftbl3 = get_function_Table_maker_St("dummy", 100, 100)();
        return run_loop(ftbl3);
    } else {
        auto ftbl3 = get_function_Table_maker_St_no_Qdt_memoization("dummy", 100, 100)();
        return run_loop(ftbl3);
    }
}

// Numerical Fisher information via the likelihood_algorithm_type variant.
// Calls calculate_mdlikelihood 2·n_params times at θ ± h_i·e_i, reads the
// recording-level score derivative(get<logL>(...)), and finite-differences
// column-by-column. See calculate_numerical_fisher_information for the
// central-difference derivation.
//
// Uses calculate_mdlikelihood (returns dMacro_State_Hessian_minimal — slim)
// rather than calculate_mdlikelihood_predictions (returns dMacro_State_Ev_
// gradient_all — full, with Evolution_of per-step). The score derivative is
// the only thing this function consumes from the returned state; both
// pipelines invoke the same Macro_DMR::log_Likelihood<…> templated on State,
// and the per-step logL accumulation in update_macro_state is identical
// regardless of State. Equivalence is asserted by
// tests/macroir/test_dlogLikelihood_equivalence.cpp (logL and score bit-
// identical between the two paths). Saves ~30-50% per F evaluation by not
// building the unused Evolution_of payload 2·p times.
auto calculate_mnumerical_fisher_information(
    const likelihood_algorithm_type& modelLikelihood_v,
    const var::Parameters_transformed& par, const Experiment& e, const Recording& r,
    double h_rel) -> Maybe_error<parameter_spd_payload> {
    auto const n = par.size();
    if (n == 0)
        return parameter_spd_payload(SymPosDefMatrix<double>(0, 0, 0.0), &par);

    Matrix<double> F_raw(n, n, 0.0);

    for (std::size_t i = 0; i < n; ++i) {
        double h_i = h_rel * std::max(std::abs(par[i]), 1.0);

        auto par_plus = par;
        par_plus[i] += h_i;
        auto par_minus = par;
        par_minus[i] -= h_i;

        auto dy_plus =
            calculate_mdlikelihood(modelLikelihood_v, par_plus, e, r);
        if (!dy_plus)
            return error_message("numerical FIM at +h, param " + std::to_string(i) + ": " +
                                 dy_plus.error()());
        auto dy_minus =
            calculate_mdlikelihood(modelLikelihood_v, par_minus, e, r);
        if (!dy_minus)
            return error_message("numerical FIM at -h, param " + std::to_string(i) + ": " +
                                 dy_minus.error()());

        auto const& s_plus = derivative(get<logL>(dy_plus.value()))();
        auto const& s_minus = derivative(get<logL>(dy_minus.value()))();

        bool col_finite = true;
        for (std::size_t j = 0; j < n; ++j) {
            double Hji = (s_plus[j] - s_minus[j]) / (2.0 * h_i);
            double Fji = -Hji;
            if (!std::isfinite(Fji)) {
                col_finite = false;
                break;
            }
            F_raw(j, i) = Fji;
        }
        if (!col_finite) {
            // NaN-propagation: AD-score had a non-finite entry while computing
            // the i-th column of the FD-of-AD Hessian. Mark the entire column
            // as NaN rather than zeroing it; symmetrization then propagates
            // NaN through the whole matrix, and the FIM's is_good() check
            // (legacy/distributions.h) flags the replicate so downstream can
            // exclude it. Trades a silent half-populated F (previous behavior)
            // for a loud, detectable failure — AD-score NaN is rare but always
            // indicates upstream numerical trouble worth investigating.
            for (std::size_t j = 0; j < n; ++j) F_raw(j, i) = std::nan("");
        }
    }

    SymPosDefMatrix<double> F(n, n, 0.0);
    for (std::size_t i = 0; i < n; ++i) {
        for (std::size_t j = i; j < n; ++j) {
            double avg = 0.5 * (F_raw(i, j) + F_raw(j, i));
            F.set(i, j, avg);
        }
    }
    return parameter_spd_payload(std::move(F), &par);
}

